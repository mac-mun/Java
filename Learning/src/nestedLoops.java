public class nestedLoops {
    public static void main(String[] args) {

//        Nested Loop = A loop inside another loop

//        Often used in Matrices or DSAs
        for (int i = 1; i <= 3; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}
